def get_processing_skill_for_ore(ore_name):
    # Try direct match
    skill = ORE_SKILL_MAP.get(ore_name)
    if skill:
        return skill
    # Try removing 'Compressed ' prefix
    if ore_name.startswith("Compressed "):
        base_ore = ore_name.replace("Compressed ", "")
        return ORE_SKILL_MAP.get(base_ore)
    # Try removing 'Batch Compressed ' prefix
    if ore_name.startswith("Batch Compressed "):
        base_ore = ore_name.replace("Batch Compressed ", "")
        return ORE_SKILL_MAP.get(base_ore)
    return None